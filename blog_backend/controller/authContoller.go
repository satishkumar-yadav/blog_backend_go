func Logout(c *fiber.Ctx) error {
	// c.ClearCookie("jwt")  // return
	cookie := fiber.Cookie{
		Name:     "jwt",
		Value:    "",                             // clear cookie value
		Expires:  time.Now().Add(-1 * time.Hour), // expire immediately
		HTTPOnly: true,
		SameSite: "Lax",
		Path:     "/",
	}
	c.Cookie(&cookie)

	return c.JSON(fiber.Map{
		"message": "Logged out successfully",
	})
}
